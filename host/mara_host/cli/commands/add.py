def to_class_name(name: str) -> str:
    """Convert name to PascalCase class name."""
    # temperature -> Temperature
    # pressure_sensor -> PressureSensor
    parts = name.replace("-", "_").split("_")
    return "".join(p.capitalize() for p in parts)


def to_feature_flag(name: str) -> str:
    """Convert name to feature flag."""
    return name.upper().replace("-", "_")


def generate_sensor(
    name: str,
    output_dir: Path,
    firmware_dir: Path,
) -> list[Path]:
    """Generate sensor skeleton files."""
    created = []
    date = datetime.now().strftime("%Y-%m-%d")

    class_name = to_class_name(name) + "Sensor"
    feature_flag = to_feature_flag(name)
    cap_name = feature_flag
    pin_prefix = feature_flag

    # Firmware sensor header
    firmware_file = firmware_dir / "include" / "sensor" / f"{class_name}.h"
    if not firmware_file.exists():
        firmware_file.parent.mkdir(parents=True, exist_ok=True)
        content = SENSOR_TEMPLATE.format(
            name=name,
            class_name=class_name,
            feature_flag=feature_flag,
            cap_name=cap_name,
            pin_prefix=pin_prefix,
            date=date,
        )
        firmware_file.write_text(content)
        created.append(firmware_file)

    # Host service
    service_name = to_class_name(name)
    service_file = output_dir / "services" / "control" / f"{name}_service.py"
    if not service_file.exists():
        service_file.parent.mkdir(parents=True, exist_ok=True)
        content = SERVICE_TEMPLATE.format(
            name=name,
            kind="sensor",
            title=service_name,
            class_name=service_name,
            date=date,
        )
        service_file.write_text(content)
        created.append(service_file)

    return created


