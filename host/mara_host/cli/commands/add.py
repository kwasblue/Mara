def run_add_command(args) -> int:
    """Run the add command."""
    kind = args.kind
    name = args.name.lower().replace("-", "_")

    # Find project directories
    # Assume we're running from host/ or the project root
    cwd = Path.cwd()

    # Try to find host dir
    if (cwd / "mara_host").exists():
        host_dir = cwd / "mara_host"
    elif (cwd / "host" / "mara_host").exists():
        host_dir = cwd / "host" / "mara_host"
    else:
        print("Error: Cannot find mara_host directory")
        print("Run this command from the host/ or project root directory")
        return 1

    # Try to find firmware dir
    if (cwd / "firmware" / "mcu").exists():
        firmware_dir = cwd / "firmware" / "mcu"
    elif (cwd.parent / "firmware" / "mcu").exists():
        firmware_dir = cwd.parent / "firmware" / "mcu"
    else:
        print("Warning: Cannot find firmware/mcu directory")
        print("Firmware files will not be generated")
        firmware_dir = None

    print(f"Adding {kind}: {name}")
    print(f"Host dir: {host_dir}")
    if firmware_dir:
        print(f"Firmware dir: {firmware_dir}")

    if kind == "sensor":
        created = generate_sensor(name, host_dir, firmware_dir)
    elif kind == "actuator":
        created = generate_actuator(name, host_dir, firmware_dir)
    else:
        print(f"Unknown kind: {kind}")
        return 1

    if not created:
        print("No files created (files may already exist)")
        return 0

    print_next_steps(kind, name, created)
    return 0


def register(subparsers):
    """Register the add command (auto-discovered by CLI)."""
    parser = subparsers.add_parser(
        "add",
        help="Scaffold new sensors or actuators",
        description="Generate skeleton files for new hardware components",
        epilog="""
Examples:
  mara add sensor temperature    # Create temperature sensor skeleton
  mara add actuator gripper      # Create gripper actuator skeleton
""",
    )
    parser.add_argument(
        "kind",
        choices=["sensor", "actuator"],
        help="Type of component to add",
    )
    parser.add_argument(
        "name",
        help="Name of the component (e.g., temperature, gripper)",
    )
    parser.set_defaults(func=run_add_command)
    return parser
