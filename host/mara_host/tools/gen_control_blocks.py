#!/usr/bin/env python3
"""
Generate control block GUI code from schema registry.

Usage:
    python mara_host/tools/gen_control_blocks.py

Or via CLI:
    mara generate control

This generates:
    - gui/widgets/block_diagram/blocks/_generated.py (auto-generated blocks)
    - gui/widgets/block_diagram/diagrams/_generated_palette.py (palette entries)

The generator reads from:
    - tools/schema/control/_controllers.py
    - tools/schema/control/_observers.py
    - tools/schema/control/_filters.py
"""

import os
from pathlib import Path
from datetime import datetime
import sys

# Direct import from schema without going through main mara_host __init__
this_dir = Path(__file__).parent
schema_dir = this_dir / "schema" / "control"
sys.path.insert(0, str(schema_dir.parent.parent))

from schema.control import CONTROL_BLOCKS, get_blocks_by_category


HEADER = '''# AUTO-GENERATED - DO NOT EDIT
# Generated by: mara generate control
# Generated at: {timestamp}
#
# To add a new control block:
#   1. Edit tools/schema/control/_controllers.py (or _observers.py, _filters.py)
#   2. Run: mara generate control
#   3. Your block will appear in the GUI palette automatically

"""Auto-generated control blocks from schema registry."""

from typing import Optional
from dataclasses import dataclass, field
from PySide6.QtCore import QPointF
from PySide6.QtGui import QColor

from ..core.block import BlockBase
from ..core.models import PortConfig, BlockConfig, PortKind, PortType


'''


def generate_block_class(block_type: str, config: dict) -> str:
    """Generate a BlockBase subclass for a control block."""
    gui = config.get("gui", {})
    params = config.get("parameters", {})
    category = config.get("category", "signal")

    class_name = "".join(word.title() for word in block_type.split("_")) + "Block"
    label = gui.get("label", block_type.title())
    color = gui.get("color", "#71717A")
    description = gui.get("description", "")
    width = gui.get("width", 60)
    height = gui.get("height", 40)

    inputs = gui.get("inputs", [])
    outputs = gui.get("outputs", [])

    # Build port configs
    input_ports = []
    for port_info in inputs:
        port_id = port_info[0]
        port_label = port_info[1] if len(port_info) > 1 else port_id.upper()
        input_ports.append(f'PortConfig("{port_id}", "{port_label}", PortKind.INPUT, PortType.SIGNAL)')

    output_ports = []
    for port_info in outputs:
        port_id = port_info[0]
        port_label = port_info[1] if len(port_info) > 1 else port_id.upper()
        output_ports.append(f'PortConfig("{port_id}", "{port_label}", PortKind.OUTPUT, PortType.SIGNAL)')

    # Build default params dict
    param_defaults = []
    for pname, pconfig in params.items():
        default = pconfig.get("default")
        if isinstance(default, str):
            param_defaults.append(f'"{pname}": "{default}"')
        elif isinstance(default, list):
            param_defaults.append(f'"{pname}": {default}')
        elif isinstance(default, bool):
            param_defaults.append(f'"{pname}": {str(default)}')
        elif default is not None:
            param_defaults.append(f'"{pname}": {default}')

    params_str = ", ".join(param_defaults) if param_defaults else ""

    code = f'''
class {class_name}(BlockBase):
    """
    {description}

    Auto-generated from schema/control registry.
    Category: {category}
    """

    block_type = "{block_type}"

    def __init__(self, block_id: str, label: str = "{label}", **kwargs):
        ports = [
            {", ".join(input_ports) + "," if input_ports else ""}
            {", ".join(output_ports) + "," if output_ports else ""}
        ]

        config = BlockConfig(
            block_id=block_id,
            block_type="{block_type}",
            label=label,
            ports=ports,
            color=QColor("{color}"),
            width={width},
            height={height},
            properties={{{params_str}}},
        )
        config.properties.update(kwargs)
        super().__init__(config)

    def get_display_text(self) -> str:
        return self.config.label

'''
    return code


def generate_palette_entries() -> str:
    """Generate palette component entries from registry."""
    lines = [
        "# AUTO-GENERATED palette entries",
        "# Add new blocks to tools/schema/control/ and regenerate",
        "",
        "GENERATED_CONTROL_COMPONENTS = [",
    ]

    # Group by category
    for category in ["controller", "observer", "filter", "signal"]:
        blocks = get_blocks_by_category(category)
        if blocks:
            lines.append(f"    # {category.upper()}S")
            for block_type, config in blocks.items():
                gui = config.get("gui", {})
                label = gui.get("label", block_type.title())
                desc = gui.get("description", "")
                color = gui.get("color", "#71717A")
                lines.append(f'    {{"type": "{block_type}", "label": "{label}", "description": "{desc}", "color": "{color}"}},')

    lines.append("]")
    return "\n".join(lines)


def generate_blocks_file(output_path: Path) -> None:
    """Generate the _generated.py blocks file."""
    timestamp = datetime.now().isoformat()

    code = HEADER.format(timestamp=timestamp)

    # Generate class for each block
    for block_type, config in CONTROL_BLOCKS.items():
        code += generate_block_class(block_type, config)

    # Add __all__ export
    class_names = []
    for block_type in CONTROL_BLOCKS:
        class_name = "".join(word.title() for word in block_type.split("_")) + "Block"
        class_names.append(class_name)

    code += "\n\n__all__ = [\n"
    for name in class_names:
        code += f'    "{name}",\n'
    code += "]\n"

    output_path.write_text(code)
    print(f"Generated: {output_path}")


def generate_palette_file(output_path: Path) -> None:
    """Generate the _generated_palette.py file."""
    timestamp = datetime.now().isoformat()

    code = f'''# AUTO-GENERATED - DO NOT EDIT
# Generated by: mara generate control
# Generated at: {timestamp}

"""Auto-generated palette entries from schema registry."""

{generate_palette_entries()}
'''

    output_path.write_text(code)
    print(f"Generated: {output_path}")


def main():
    """Run the control block generator."""
    # Determine output paths
    this_dir = Path(__file__).parent
    host_dir = this_dir.parent

    blocks_output = host_dir / "gui" / "widgets" / "block_diagram" / "blocks" / "_generated.py"
    palette_output = host_dir / "gui" / "widgets" / "block_diagram" / "diagrams" / "_generated_palette.py"

    # Ensure directories exist
    blocks_output.parent.mkdir(parents=True, exist_ok=True)
    palette_output.parent.mkdir(parents=True, exist_ok=True)

    # Generate files
    generate_blocks_file(blocks_output)
    generate_palette_file(palette_output)

    print(f"\nGenerated {len(CONTROL_BLOCKS)} control blocks from registry.")
    print("\nTo use in palette, import GENERATED_CONTROL_COMPONENTS from _generated_palette")
    print("To use blocks, import from _generated.py")


if __name__ == "__main__":
    main()
